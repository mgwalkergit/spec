class Model:
    def __init__(self, model_paras_file, model_paras_path=None, device=None):
        if model_paras_path is None:
            model_paras_path = Path(".")
        if device is None:
            device = "cuda" if torch.cuda.is_available() else "cpu"
        self.device = torch.device(device)
        saved_state_dict = torch.load(
            Path(model_paras_path).joinpath(
                model_paras_file,
            ),
            map_location=self.device,
        )
        self.model = model_selector[saved_state_dict["model_name"]](
            ndim_input=4,
            ndim_output=saved_state_dict["ndim_output"],
            **saved_state_dict["hyperparameters_dict"]["model_kwds"],
        )
        self.model.load_state_dict(saved_state_dict["model_state"])
        self.model.to(self.device)
        self.model.eval()
        self.data_min = torch.FloatTensor([3.9e03, 0.0e00, -4.0e00, -1.0e00]).to(
            self.device
        )
        self.data_max_minus_min = torch.FloatTensor(
            [3.6e03, 5.0e00, 5.0e00, 2.4e00]
        ).to(self.device)
        # print(model)

    @staticmethod
    def _to_numpy(data, dtype=np.float32):
        if torch.is_tensor(data):
            return data.detach().cpu().numpy().astype(dtype=dtype, copy=False)
        else:
            return np.array(data, dtype=dtype, copy=False)

    @staticmethod
    def _to_tensor_with_copy(data, device):
        if torch.is_tensor(data):
            return data.detach().clone().to(torch.float32).to(device)
        else:
            return torch.tensor(data, dtype=torch.float32).to(device)

    def __call__(self, input_paras):
        input_paras_tensor = self._to_tensor_with_copy(input_paras, self.device)
        # normalize input paras
        input_paras_tensor = (
            input_paras_tensor - self.data_min
        ) / self.data_max_minus_min
        # predict
        spectrum = self.model(input_paras_tensor)
        # data type torch.tensor -> numpy.ndarray
        if not torch.is_tensor(input_paras):
            spectrum = self._to_numpy(spectrum, dtype=input_paras.dtype)
        return spectrum

