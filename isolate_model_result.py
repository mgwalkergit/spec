import collections.abc
from pathlib import Path

import torch
import torch.nn as nn
import numpy as np



_activation_dict = {
    "tanh": lambda: nn.Tanh(),
    "leaky_relu": lambda: torch.nn.LeakyReLU(),
}


class BaseModel(nn.Module):
    def __init__(self) -> None:
        super().__init__()

    def forward(
        self,
    ):
        raise NotImplementedError


class FullyConnected(nn.Module):
    def __init__(
        self,
        ndim_input,
        ndim_output,
        activation="tanh",
        flag_bn=True,
        flag_drop_out=False,
        p_dropout=0.1,
        bn_kwds=None,
    ):
        if bn_kwds is None:
            bn_kwds = {}
        super().__init__()
        self.seq = nn.Sequential(
            nn.Linear(ndim_input, ndim_output), _activation_dict[activation]()
        )
        if flag_bn:
            self.seq.append(nn.BatchNorm1d(num_features=ndim_output, **bn_kwds))
        if flag_drop_out:
            self.seq.append(nn.Dropout(p=p_dropout))

    def forward(self, x):
        return self.seq(x)


class MLP(BaseModel):
    def __init__(
        self,
        ndim_input,
        ndim_output,
        ndim_layers,
        activation="tanh",
        flag_bn=True,
        flag_drop_out=False,
        p_dropout=0.25,
        bn_kwds=None,
    ) -> None:
        super().__init__()
        if bn_kwds is None:
            bn_kwds = {}
        if not isinstance(ndim_layers, collections.abc.Iterable):
            ndim_layers = [ndim_layers]
        self.activation = activation
        self.ndim_input = ndim_input
        self.ndim_output = ndim_output
        self.ndim_layers = list(ndim_layers)
        # Construct layers to the second to the last
        self.sequence_model = nn.Sequential()
        for i, j in zip([ndim_input] + self.ndim_layers[:-1], self.ndim_layers):
            self.sequence_model.append(
                FullyConnected(
                    i,
                    j,
                    activation=activation,
                    flag_bn=flag_bn,
                    flag_drop_out=flag_drop_out,
                    p_dropout=p_dropout,
                    bn_kwds=bn_kwds,
                )
            )
        # Construct layer without activation
        self.sequence_model.append(nn.Linear(self.ndim_layers[-1], ndim_output))

    def forward(self, x):
        output = self.sequence_model.forward(x)
        return output


def clamp_decorator(cls):
    class Clamped(cls):
        def forward(self, x):
            result = super().forward(x)
            if not self.training:
                result = torch.clamp(result, min=0.0, max=1.0)
            return result

    return Clamped


model_selector = {
    "MLP": MLP,
    "MLP_clamped": clamp_decorator(MLP),
    # "RBFInterpolator": RBFInterpolator,
}


def resave_as_final_model_paras(model_paras, model_paras_path=None):
    if model_paras_path is None:
        model_paras_path = Path(".")
    saved_state_dict = torch.load(
        model_paras_path.joinpath(
            model_paras,
        )
    )
    torch.save(
        {
            i: saved_state_dict[i]
            for i in saved_state_dict
            if i in ["model_name", "hyperparameters_dict", "model_state", "ndim_output"]
        },
        model_paras_path.joinpath(f"final_{model_paras}"),
    )


